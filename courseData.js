export const extractCourseId = async (
  backendUrl,
  dauth,
  examDetails,
  subjects = {}
) => {
  const data = { data: [] };

  try {
    const response = await fetch(`${backendUrl}/platform/platformcourse`, {
      method: "GET",
      headers: {
        // Authorization: `Bearer ${token}`,
        dauth: dauth,
      },
    });

    if (response.ok) {
      data.data = await response.json();
    }
  } catch (error) {
    console.error(error);
  }
  //fetch level and examType directly , extract session

  const level = examDetails.level;
  const examType = "CFA";
  let examYear = examDetails?.examDetails?.split(" ")[0].toString();
  let sessionPart =
    examDetails?.examDetails?.split(" ")[1]?.substring(0, 3) || "";
  let examSession =
    sessionPart.charAt(0).toUpperCase() + sessionPart.slice(1).toLowerCase();

  let subjectL3;
  if (level === 3 && examType === "CFA") {
    const targetSubjects = [
      "Portfolio Management",
      "Private Markets",
      "Private Wealth",
    ];
    subjectL3 = targetSubjects.find((subject) => subject in subjects);
  }

  if (examType === "FRM") {
    examSession = examDetails?.session?.split(" ")[0]?.substring(0, 3);
    examYear = examDetails?.session?.split(",")[1]?.substring(0, 4);
  }

  let id;
  //   console.log("examYear", examYear);
  //   console.log("examSession", examSession);
  //   console.log("level", level);
  //   console.log(examDetails);
  if (examType === "CFA") {
    if (level === 1) {
      const courseYearObj = data?.data[0]?.Courses[level - 1]?.Courses;
      // console.log("courseYearObj ", courseYearObj);
      const courseMonthObj = courseYearObj?.find((d) => d.name === examYear);

      // console.log("courseMonthObj", courseMonthObj);
      const courseObj = courseMonthObj?.Courses?.find(
        (d) => d.name === examSession
      );

      id = courseObj?.id;
    }

    if (level === 2) {
      const courseYearObj = data?.data[0]?.Courses[level - 1]?.Courses;
      // console.log("courseYearObj ", courseYearObj);
      const courseMonthObj = courseYearObj?.find((d) => d.name === examYear);

      // console.log("courseMonthObj", courseMonthObj);
      const courseObj = courseMonthObj?.Courses?.find(
        (d) => d.name === examSession
      );

      id = courseObj?.id;
    }

    if (level === 3) {
      const courseYearObj = data?.data[0]?.Courses[level - 1]?.Courses;
      // console.log("courseYearObj ", courseYearObj);
      const courseMonthObj = courseYearObj
        ?.flatMap((course) => course?.Courses || [])
        .find((d) => d.name === examYear);

      // console.log("courseMonthObj", courseMonthObj);
      const courseObj = courseMonthObj?.Courses?.find(
        (d) => d.name === examSession
      );

      id = courseObj?.id;
    }
    console.log("id", id);
  } else if (examType === "FRM") {
    const courseYearObj = response?.data[1]?.Courses[level - 1]?.Courses;

    const courseMonthObj = courseYearObj?.find(
      (course) => course?.name === examYear
    )?.Courses;
    id = courseMonthObj?.find((course) => course?.name == examSession)?.id;
  }

  return id;
};
export default extractCourseId;
